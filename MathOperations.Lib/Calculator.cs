using System;

namespace MathOperations.Lib
{
    public class Calculator
    {
        public int Plus(int a, int b)
        {
            return a + b;
        }
        public int Minus(int a, int b)
        {
            return a - b;
        }
        public int Umnozhit(int a, int b)
        {
            return a * b;
        }
        public int Delit(int a, int b)
        {
            return a / b;
        }
    }
}
